#include "libft.h"

char	*ft_substr(char const *s, unsigned int start, size_t len)
{
	char	*new;
	size_t	i;	

	if (!s)
		return (NULL);
	if ((size_t)start > ft_strlen(s))
		return ("");
	new = malloc(sizeof(char) * (len + 1));
	i = 0;
	if (!new)
		return (NULL);
	while (i < len)
	{
		new[i] = s[start + i];
		i++;
	}
	new[i] = '\0';
	return (new);
}
